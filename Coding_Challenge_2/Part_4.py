# 4. User input
# Ask the user for an input of their current age, and tell them how many years
# until they reach retirement (65 years old).
#
# Hint:

age = input("What is your age? ")
print("Your age is " + str(age))
int_age = int(age)

yrs_til_retirement = 65 - int_age

print("You have " + str(yrs_til_retirement) + " years until retirement")
